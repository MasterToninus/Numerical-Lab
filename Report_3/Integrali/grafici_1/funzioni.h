double funzA(double x){
double y= (1-cos(2*x))/(x*x);
return(y);}

double funzB(double x){
double y= 1/sqrt((double)25-x*x);
return(y);}

double funzC(double x){
double y= sin(x);
return(y);}

double funzD(double x){
double y= log(x);
return(y);}

double funzE(double x){
double y= exp(-x*x-1/(x*x));
return(y);}

double funzF(double x){
double y= log(x)/(1+x);
return(y);}

double funzG(double x){
double y= 1/pow(x,x);
return(y);}
